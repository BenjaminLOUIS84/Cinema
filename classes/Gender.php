<?php

class Gender{
    ////////////////////////////////////////////Attributes///////////////////////////////////////////
    private string $type;
     
    ////////////////////////////////////////////Constructor///////////////////////////////////////////

    public function __construct(string $type){
        
        $this->type = $type;
     }
    ////////////////////////////////////////////Getter & Setter///////////////////////////////////////////

    public function getType():string
    {
        return $this->type;
    }

    public function setType($type)
    {
        $this->type = $type;
        return $this;
    }
    ////////////////////////////////////////////Method///////////////////////////////////////////

    public function getInfos(){ 
        return "Genre :" .$this->getType();
    }

}

?>



